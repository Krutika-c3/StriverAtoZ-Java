package com.dsa.striver_a_to_z.step1.lec1;

public class DataTypes {
    public static void main(String[] args) {
        // Example of using different data types in Java
        int integerNumber = 10; // Integer type
        double doubleNumber = 20.5; // Double type
        char character = 'A'; // Character type
        boolean isTrue = true; // Boolean type
        float floatNumber = 15.75f; // Float type
        long longNumber = 123456789L; // Long type
        short shortNumber = 32000; // Short type
        byte byteNumber = 127; // Byte type
        String text = "Hello, Java!"; // String type

        // Printing the values of different data types
        System.out.println("Integer: " + integerNumber);
        System.out.println("Double: " + doubleNumber);
        System.out.println("Character: " + character);
        System.out.println("Boolean: " + isTrue);
        System.out.println("Float: " + floatNumber);
        System.out.println("Long: " + longNumber);
        System.out.println("Short: " + shortNumber);
        System.out.println("Byte: " + byteNumber);
        System.out.println("String: " + text);
    }
}